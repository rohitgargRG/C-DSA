import java.util.*;

public class one{
    public static void main(String[] args){
        
        // taking input
        Scanner sc = new Scanner(System.in);
        
        // take integer input
        int n = sc.nextInt();
        sc.nextLine();
        
        // print
        System.out.println("integer output : " + n);
        
        // take string input
        String s1 = sc.nextLine();
        
        System.out.println("string 1 : " + s1);
        
        
        String s2 = sc.next();
        
        System.out.println("string 2 : " + s2);
    }
}