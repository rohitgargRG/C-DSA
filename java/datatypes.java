import java.util.*;

public class datatypes{
    public static void main(String[] args){

        // byt1 - 1 byte
        byte a = 1;

        // short - 2 bytes
        short b = 2;

        // int - 4 bytes
        int c = 10;

        // long - 8 bytes
        long d = 100;

        // char - 2 bytes
        char e = 'A';

        // float - 4 bytes
        float f = 12.12;

        // double  - 8 bytes
        double g = 100.100;

        // boolean - t/f
        boolean h = true;
    }
}